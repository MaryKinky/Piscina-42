#include <unistd.h>

int	ft_countdown(void)

{
	char	i;

	i = '0';
	while (i >= '0' && i <= '9')
	{
		write(1, &i, 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}

int	main(void)

{
	ft_countdown();
}

