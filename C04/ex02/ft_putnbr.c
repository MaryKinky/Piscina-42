#include <unistd.h>

void	ft_putnbr(int nb)

{
	char	a;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = nb * -1;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		if (nb <= 9)
		{
			a = nb + '0';
			write(1, &a, 1);
		}
	}
}

int	main(void)

{
	ft_putnbr(2547);
	write(1, "\n", 1);
}
